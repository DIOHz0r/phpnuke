function translate($phrase) {
    switch($phrase) {
	case "xdatestring":	$tmp = "%d-%m-%Y o %T"; break;
	case "linksdatestring":	$tmp = "%d-%m-%Y"; break;
	case "xdatestring2":	$tmp = "%d-%m-%Y"; break;
	default:		$tmp = "$phrase"; break;
    }
    return $tmp;
}
